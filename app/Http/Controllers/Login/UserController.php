     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'id' => 'required',
            
        ],[
            'name.required' => 'Please enter your name'
            
            
        ]);
        User::create([
            'name'=> $request->name,
            'email'=> $request->email,
            'student_id'=>$request->id,
            'shift'=>$request->shift,
            'gender'=>$request->gender,
        ]);
        return redirect()->route('home');
    }

